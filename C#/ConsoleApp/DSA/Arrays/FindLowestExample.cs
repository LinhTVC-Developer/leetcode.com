namespace ConsoleApp.DSA.Arrays;

public static class FindLowestExample
{
    public static void Main(string[] args)
    {
        int[] arrInt = { 7, 12, 9, 4, 11 };
        int lowest = arrInt[0];
        for (int i = 0; i < arrInt.Length-1; i++)
        {
            if(lowest > arrInt[i])
            {
                lowest = arrInt[i];
            }
        }
        Console.WriteLine(lowest);
    } 
}
